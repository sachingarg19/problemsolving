package com.equals;

/**
 * @author Sachin Garg
 *
 */
public class EqualsUtil {

	public static boolean equalString(String a, String b){
		return a == b;
	}

	public static boolean equalInt(int i, int j){
		return i == j;
	}

	public static boolean equalObj(Object i, Object j){
		return i == j;
	}

	public static boolean equalLong(long i, long j){
		return i == j;
	}

	public static boolean equalDouble(double i, double j){
		return i == j;
	}
}
